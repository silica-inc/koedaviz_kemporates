// 全国平均
export const nationalAvg = +(
  prefectureData.reduce((sum, p) => sum + p.healthRate, 0) /
  prefectureData.length
).toFixed(2);

// ランキング (高い順 = 1位が最高料率)
export const rankedData = [...prefectureData].sort(
  (a, b) => b.healthRate - a.healthRate,
);
export const rankMap = new Map(rankedData.map((p, i) => [p.id, i + 1]));

// 最小・最大
export const minRate = Math.min(...prefectureData.map((p) => p.healthRate));
export const maxRate = Math.max(...prefectureData.map((p) => p.healthRate));
